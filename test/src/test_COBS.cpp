#define EncodeTest(n) \
TEST(COBS, Encode##n) \
{\
	uint8_t working[sizeof(decoded##n) + 1];\
\
	memcpy(working + 1, decoded##n, sizeof(decoded##n));\
\
	COBS::Encode(working + 1, sizeof(decoded##n));\
	ASSERT_THAT(working, testing::ElementsAreArray(encoded##n));\
}

#define DecodeTest(n) \
TEST(COBS, Decode##n) \
{\
	uint8_t working[sizeof(encoded##n)];\
\
	memcpy(working, encoded##n, sizeof(encoded##n));\
\
	ASSERT_TRUE(COBS::Decode(working, sizeof(encoded##n)));\
	ASSERT_THAT(std::vector<uint8_t>(working + 1, working + 1 + sizeof(decoded##n)),\
		testing::ElementsAreArray(decoded##n));\
}

